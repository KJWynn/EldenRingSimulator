package game.capabilities;

/**
 * An enum to store all the enemy types.
 * @author Khor Jia Wynn,Saw Tze Ying,Wong An Hong
 */
public enum EnemyType {
    /**
     * Canine enemy types, e.g. GiantDog and LoneWolf
     */
    CANINE,
    /**
     * Ocean enemy types, e.g. GiantCrab and GiantCrayfish
     */
    OCEAN,
    /**
     * Skeletal enemy types, e.g. HeavySkeletalSwordsman and SkeletalBandit
     */
    SKELETAL,
    /**
     * Stormveil Castle enemy types
     */
    STORMVEIL_CASTLE_ENEMY,
    /**
     * Invader enemy types
     */
    INVADER
}
